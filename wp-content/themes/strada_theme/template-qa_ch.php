                                          ・保険関係成立届など、事業主が行う各種書類手続きの代行<br/>
                                          ・概算保険料、確定保険料、その他労働保険料に関わる徴収金の申告手続き<br/>
                                          ・雇用保険被保険者に関する届出（資格取得・資格喪失・離職証明書の作成、氏名変更などのハローワーク（公共職業安定所）への届出、手続き。
