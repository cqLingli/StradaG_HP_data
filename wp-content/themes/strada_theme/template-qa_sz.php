            <p class="c-qa__question--text">株式会社設立は自分で行うこともできますか？</p>
          </div>
          <div class="c-qa__answer">
            <div class="c-qa__answer___main">
              <p class="c-qa__answer--icon">A</p>
              <p class="c-qa__answer--text">株式会社設立はご自身で行うことは可能です。ただし、専門的な知識が必要ですし、料金も幣事務所が実施するのと同等な価格です。
                                            そのため、９９％の方が専門家に依頼しているのが実情です。会社設立の手順は、以下のような実施していきます。<br />
                                            <br />
                                            ①会社設立準備<br />
                                            会社設立における基礎情報を決定（会社名、本店所在地、事業目的、株主、役員、資本金、事業年度等）する会社名が決定した場合に、社判を購入する<br />
                                            <br />
                                            ②定款の作成/認証<br />
                                            定款をご自身又は専門家によって作成してもらう<br />
                                            ※定款とは、会社の憲法であり、必ず作成しなければならない書類の一つです。簡単に言うと、ご自身の会社のルールを決定した書面です。<br />
                                            公証人役場により、定款を認証してもらう。公証人役場は、本店所在地が東京であれば、どこの公証人役場で認証頂いても大丈夫です。
                                            <br />
                                            ③登記書類の作成<br />
                                            「①会社設立準備」において、決定した資本金の額を発起人の個人口座に入金し、それのコピーをとる。払込み口座はネットバンキングでもよい。
                                            登記に必要な書類を作成します。これは、定款にどこまで記載するか、又は、機関設計等によって必要な書類が変わってきます。<br />
                                            <br />
                                            ④会社設立登記<br />
                                            法務局に登記を行います。登記簿謄本が出来上がるまで、１週間程係る場合があります。<br />
                                            <br />
                                            ⑤開業の届出等<br />
                                            登記簿謄本が出来上がりましたら、必要書類を持参の上、銀行口座を開設手続きにはいります。登記完了後も、必要書類を各種<br />
                                            <br />
                                            ⑥設立完了<br />
                                            ここからが、会社運営のはじまりです。もし、創業補助金等を申請される場合は、申請手続きに入る必要がございます。
              </p>
