            <p class="c-qa__question--text">設立後に税理士に係る費用はどれくらい？</p>
          </div>
          <div class="c-qa__answer">
            <div class="c-qa__answer___main">
              <p class="c-qa__answer--icon">A</p>
              <p class="c-qa__answer--text">税理士にお支払する報酬としては、以下4点となります。<br />

                                            （1）毎月の顧問料・記帳代行料
                                            ⇒毎月、税務相談や会計相談を実施するための費用となります。税理士に記帳代行も頼まれる場合は、記帳代行料もかかることになります。<br />

                                            （2）決算料
                                            ⇒会社運営をされている限りでは、決算書・勘定科目内訳書・概況報告書・法人税の申告書・地方税の申告書・消費税の申告書等を毎年一回作成して、税務署等に送付する必要があります。そのために、決算書等を作成する必要があります。<br />

                                            （3）年末調整・法定調書の作成代行
                                            ⇒年末調整や法定調書の作成を税理士に依頼する場合は、こちらの金額も支払う必要があります。<br />

                                            （4）税務調査の立会料
                                            ⇒税務調査に入った場合に、顧問税理士も立ち会う必要がございます。その際、1日中拘束される場合もありますので、その人件費を支払う必要がございます。<br />

                                            会社でどこまでの業務を実施するのか、また、会社の年商や取引の複雑性・仕訳数に応じて、金額も増減致します。
              </p>
